import json, random, time
from pathlib import Path
from openai import OpenAI

BASE_URL = "http://localhost:1234/v1"
MODEL = "local-model"  # выбери в LM Studio
OUT = Path("data/dataset_200.jsonl")

client = OpenAI(base_url=BASE_URL, api_key="lm-studio")

SYSTEM = (
    "Ты бизнес-аналитик российского маркетплейса (аналог Ozon/WB, без упоминания брендов). "
    "Пиши строго по шаблону:\n"
    "1) Краткое описание (1–2 абзаца)\n"
    "2) Цель процесса\n"
    "3) Участники и ответственность\n"
    "4) Входные условия\n"
    "5) Основной поток (шаги 1..N)\n"
    "6) Статусы и переходы\n"
    "7) Сроки / SLA\n"
    "8) Исключения и спорные случаи\n"
    "9) Что логируем и какие уведомления отправляем\n\n"
    "Термины: 'со склада маркетплейса', 'со склада продавца', ПВЗ, курьер, служба доставки, поддержка, финансы. "
    "Без FBO/FBS. Пиши развёрнуто, но без воды."
)

SEEDS = [
    "Возврат товара надлежащего качества через ПВЗ (товар со склада маркетплейса).",
    "Возврат товара с браком: покупатель получил курьером (товар со склада продавца).",
    "Отказ покупателя при получении в ПВЗ (товар со склада продавца).",
    "Отмена заказа до сборки (товар со склада маркетплейса).",
    "Отмена заказа после передачи в доставку (товар со склада маркетплейса).",
    "Частичный возврат: в заказе несколько позиций, возвращают одну.",
    "Недовложение/пересорт: покупатель открыл обращение.",
    "Утеря посылки на доставке: кто компенсирует и как закрывается кейс.",
    "Штраф продавцу за просрочку отгрузки (товар со склада продавца).",
    "Корректировка комиссии и взаиморасчётов после возврата.",
    "Компенсация покупателю за просрочку доставки: правила и ограничения.",
    "Спор покупатель–продавец: эскалация и арбитраж маркетплейса."
]

def chat(messages, temperature=0.6):
    r = client.chat.completions.create(
        model=MODEL,
        messages=messages,
        temperature=temperature,
    )
    return r.choices[0].message.content

def make_prompt(seed: str) -> str:
    mode = random.random()
    if mode < 0.4:
        return f"Опиши процесс: {seed}"
    if mode < 0.9:
        return (
            f"Ответь на вопрос по процессу '{seed}': "
            f"{random.choice(['Какие статусы и переходы должны быть предусмотрены?',
                              'Кто инициирует процесс и кто подтверждает результат?',
                              'Какие SLA и где фиксируются дедлайны?',
                              'Какие бывают исключения и как эскалировать в поддержку?',
                              'Что делать, если статус не меняется более 48 часов?',
                              'Какие уведомления отправляем покупателю и продавцу?'])} "
            "Ответ строго по шаблону."
        )
    return (
        f"Разбери крайний случай по процессу '{seed}': "
        "есть конфликт между покупателем и продавцом, сроки вышли, доказательства неоднозначны. "
        "Опиши как процесс должен отработать: статусы, решения, кто принимает финальное решение. "
        "Ответ по шаблону."
    )

def main(n=200):
    OUT.parent.mkdir(parents=True, exist_ok=True)
    with OUT.open("w", encoding="utf-8") as f:
        for i in range(n):
            seed = random.choice(SEEDS)
            user = make_prompt(seed)

            assistant = chat(
                [
                    {"role": "system", "content": SYSTEM},
                    {"role": "user", "content": user},
                ],
                temperature=0.6,
            )

            rec = {
                "messages": [
                    {"role": "system", "content": SYSTEM},
                    {"role": "user", "content": user},
                    {"role": "assistant", "content": assistant},
                ]
            }
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")

            if (i + 1) % 25 == 0:
                print(f"generated {i+1}/{n}")
            time.sleep(0.05)

if __name__ == "__main__":
    main()